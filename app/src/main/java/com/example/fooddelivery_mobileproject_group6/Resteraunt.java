package com.example.fooddelivery_mobileproject_group6;

public class Resteraunt {
    private String resterauntId;
    private String resterauntName;
    private String image;

    public String getResterauntId() {
        return resterauntId;
    }

    public void setResterauntId(String resterauntId) {
        this.resterauntId = resterauntId;
    }

    public String getResterauntName() {
        return resterauntName;
    }

    public void setResterauntName(String resterauntName) {
        this.resterauntName = resterauntName;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
